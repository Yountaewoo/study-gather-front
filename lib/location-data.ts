// 헬퍼 함수들
export function getProvinces(): Location[] {
  return locations.filter((loc) => loc.parentId === null)
}

export function getDistricts(provinceId: number): Location[] {
  return locations.filter((loc) => loc.parentId === provinceId)
}

export function getLocationById(id: number): Location | undefined {
  return locations.find((loc) => loc.id === id)
}

export function getLocationNameById(id: number): string {
  const location = getLocationById(id)
  return location ? location.name : "알 수 없는 지역"
}

export const locationData = getProvinces().map((province) => ({
  ...province,
  districts: getDistricts(province.id),
}))
